package com.qianjing.finance.model.redeem;

import com.qianjing.finance.model.common.BaseModel;

/**
 * @author renzhiqiang
 * @function 组合赎回参数类
 *
 */
public class AssembleShares extends BaseModel
{

	private static final long serialVersionUID = 1L;

	/**
	 * 组合赎回份额
	 */
	private String share;
	/**
	 * 组合名称
	 */
	private String assembleName;
	/**
	 * 每只基金价值
	 */
	private String shareMoney;

	public AssembleShares(String share, String assembleName, String shareMoney)
	{

		this.share = share;
		this.assembleName = assembleName;
		this.shareMoney = shareMoney;
	}

	public String getShare()
	{
		return share;
	}

	public void setShare(String share)
	{
		this.share = share;
	}

	public String getAssembleName()
	{
		return assembleName;
	}

	public void setAssembleName(String assembleName)
	{
		this.assembleName = assembleName;
	}

	public String getShareMoney()
	{
		return shareMoney;
	}

	public void setShareMoney(String shareMoney)
	{
		this.shareMoney = shareMoney;
	}

}
